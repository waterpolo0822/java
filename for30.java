package Joji_package;

public class for30 {
	public static void main(String[] args)
	{
		
		for(int a=5; a>=1; a--)
		{
			for(int c=5; c>a; c--)
			{
				System.out.print(" ");
			}
			for(int b=1; b<=a; b++)
			{
				System.out.print(b);
			}
			for(int d=a-1; d>=1; d--)
			{
				System.out.print(d);
			}
		System.out.println();	
		}
	}

}
