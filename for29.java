package Joji_package;

public class for29 {
	public static void main(String[] args)
	{
		for(int a=1; a<=5; a++)
		{
			for(int b=4; b>=a; b--)
			{
				System.out.print(" ");
			}
			for(int c=1; c<=a; c++)
			{
				System.out.print(c);
			}
			
			for(int d=a-1; d>=1; d--)
			{
				System.out.print(d);
			}
		System.out.println();
		}
	}

}
