package Joji_package;

public class for27 {
	public static void main(String[] args){
		int d=4;
		for(int a=1;a<=9;a++)
		{
			if(a<=5)
			{
				for(int b=1;b<=a;b++)
				{
					System.out.print(b);
				}
			}
			else
			{
				for(int c=1;c<=d;c++)
				{
					System.out.print(c);
				}
				d--;
			}
		System.out.println();
		
		}
	}

}
